    private void placeReservation() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yy-MM-dd");
            ReservationDto dto = new ReservationDto(txtReservationId.getText(), txtCustId.getText(),
                    sdf.format(new Date()), resDetailDtos);
            String resp = reservationController.placeResvervation(dto);
            JOptionPane.showMessageDialog(this, resp);
        } catch (Exception ex) {
            Logger.getLogger(ReservationView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error");
        }
    }

    private void searchItem() {
        try {
            String itemId = txtRoomId.getText();
            RoomDto itemDto = itemController.get(itemId);
            if (itemDto != null) {
                lblItemData.setText(itemDto.getCategory() + " | " + itemDto.getUnitPrice() + " | " + itemDto.getQoh());
            } else {
                JOptionPane.showMessageDialog(this, "Item Not Found");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error");
            Logger.getLogger(ReservationView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void loadTable(){
        String [] columns = {"RoomId", "Qty", "Discount"};
        DefaultTableModel dtm = new DefaultTableModel(columns, 0){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
            
        };
        tblRoom.setModel(dtm);
    }
