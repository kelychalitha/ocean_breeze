    private void addToTable() {
        ResDetailDto resDetailDto = new ResDetailDto();
        resDetailDto.setRoomId(txtRoomId.getText());
        resDetailDto.setQty(Integer.parseInt(txtQty.getText()));
        resDetailDto.setDiscount(Double.parseDouble(txtDiscount.getText()));
        
        resDetailDtos.add(resDetailDto);
        
        Object[] rowData = {resDetailDto.getRoomId(), resDetailDto.getQty(), resDetailDto.getDiscount()};
        
        DefaultTableModel dtm = (DefaultTableModel) tblRoom.getModel();
        dtm.addRow(rowData);
        clearRoomData();
    }
    
